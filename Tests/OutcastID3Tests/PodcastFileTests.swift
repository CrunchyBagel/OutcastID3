    /// Verifies that parsing a real-world ID3v2.4 tag with chapters does not
    /// crash due to the out-of-bounds offset bug in readString.
    func testParseDoByFridayEpisode() throws {
        let data = Data(PodcastFileTests.doByFridayID3Bytes)

        // Write to a temp file so MP3File can read it via FileHandle
        let tmpUrl = FileManager.default.temporaryDirectory
            .appendingPathComponent("OutcastID3Test_\(UUID().uuidString).mp3")
        try data.write(to: tmpUrl)
        defer { try? FileManager.default.removeItem(at: tmpUrl) }

        let mp3 = try OutcastID3.MP3File(localUrl: tmpUrl)
        let tagProperties = try mp3.readID3Tag()
        let tag = tagProperties.tag

        // The v2.4 parser recovers gracefully from the synch-safe size
        // mismatch in this tag-only data, parsing the initial string frames.
        // The key assertion is that parsing completes without crashing.
        XCTAssertFalse(tag.frames.isEmpty, "Should parse frames from real podcast ID3 data")

        let stringFrames = tag.frames.compactMap { $0 as? OutcastID3.Frame.StringFrame }
        XCTAssertGreaterThanOrEqual(stringFrames.count, 1,
            "Should parse at least one string frame")
    }
